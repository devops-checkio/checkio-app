import { v5 as uuidv5 } from "uuid";

const FIELD_NAMESPACE = "6ba7b810-9dad-11d1-80b4-00c04fd430c8";

function generateFieldId(seed: string): string {
  return uuidv5(seed, FIELD_NAMESPACE);
}

export enum ReportStatus {
  PENDING = "PENDING",
  COMPLETED = "COMPLETED",
  FAILED = "FAILED",
  SCHEDULED = "SCHEDULED",
  PROCESSING = "PROCESSING",
}

export enum FieldCategory {
  EMPLOYEE = "EMPLOYEE",
  POSITION = "POSITION",
  BRANCH = "BRANCH",
  COMPANY = "COMPANY",
  SHIFT = "SHIFT",
  SCHEDULE = "SCHEDULE",
  ABSENCE = "ABSENCE",
  ATTENDANCE = "ATTENDANCE",
  OVERTIME = "OVERTIME",
  HOLIDAY = "HOLIDAY",
}

export enum ReportPeriodicity {
  DAILY = "DAILY",
  WEEKLY = "WEEKLY",
  MONTHLY = "MONTHLY",
  QUARTERLY = "QUARTERLY",
  CUSTOM = "CUSTOM",
}

export enum ReportTimeUnit {
  DAYS = "DAYS",
  WEEKS = "WEEKS",
  MONTHS = "MONTHS",
}

export enum ReportRecipientType {
  EMPLOYEE = "EMPLOYEE",
  MANAGER = "MANAGER",
  CUSTOM = "CUSTOM",
}

export enum TotalRowType {
  SUM = "SUM",
  AVG = "AVG",
  COUNT = "COUNT",
  MIN = "MIN",
  MAX = "MAX",
  CUSTOM = "CUSTOM",
}

export interface ReportField {
  id: string;
  name: string;
  displayName: string;
  type: string;
  table: string;
  category: FieldCategory;
  selected: boolean;
  order: number;
  columnIndex?: number;
  color?: string;
  conditionalColor?: {
    field: string;
    operator: "equals" | "contains" | "greater" | "less";
    value: string | number;
    color: string;
  }[];
  description?: string;
  isSortable?: boolean;
  isFilterable?: boolean;
  aggregationType?: "sum" | "avg" | "count" | "min" | "max" | "none";
  format?: string;
  timeFormat?: string;
  isConsolidated?: boolean;
  consolidatedFields?: string[];
  consolidatedFieldIds?: string[];
  consolidatedSeparator?: string;
  originalTags?: string[];
  consolidatedFieldFormats?: {
    fieldId: string;
    format?: string;
    timeFormat?: string;
  }[];
  isCalculated?: boolean;
  calculatedFields?: string[];
  calculatedFieldIds?: string[];
  calculationType?: "sum" | "subtract" | "multiply" | "divide" | "average" | "custom";
  formula?: string; // Para cálculos personalizados (ej: "A - B", "before - after")
  headerTransform?: "DEFAULT" | "UPPER" | "LOWER"; // Transformación del título de la columna
}

export interface ReportSchedule {
  periodicity: ReportPeriodicity;
  timeUnit?: ReportTimeUnit;
  interval?: number;
  daysOfWeek?: number[]; // 0-6 (Sunday-Saturday)
  dayOfMonth?: number; // 1-31
  weekOfMonth?: number; // 1-5
  time?: string; // HH:mm format
  startDate?: string;
  endDate?: string;
}

export interface ReportNotification {
  enabled: boolean;
  recipientType: ReportRecipientType;
  recipients?: string[]; // Email addresses
  includeAttachment: boolean;
  subject?: string;
  message?: string;
}

export interface ReportTemplate {
  id: string;
  name: string;
  description: string;
  fields: ReportField[];
  schedule: ReportSchedule;
  notification: ReportNotification;
  createdAt: string;
  createdBy: string;
  status: ReportStatus;
}

export interface ReportResponseDto {
  publicId: string;
  name: string;
  description?: string;
  fields?: ReportField[];
  createdAt: string | Date;
  updatedAt: string | Date;
  createdBy: string;
  status: ReportStatus | string;
  downloadUrl?: string;
  completedAt?: string | Date;
  fileSize?: number;
  fileFormat?: string;
  fileExtension?: string;
  fileContentType?: string;
  totalSteps?: number;
  currentStep?: number;
  stepDescription?: string;
  emailSent?: boolean;
  emailRecipients?: string[];
  processingDuration?: number;
}

export interface ReportCreateDto {
  name: string;
  description: string;
  fields: ReportField[];
  totalRows?: TotalRow[];
}

export interface ReportFilterDto {
  search?: string;
  status?: ReportStatus;
  startDate?: string;
  endDate?: string;
  page?: number;
  pageSize?: number;
  sort?: "asc" | "desc";
}

export interface TotalRowColumn {
  fieldId: string;
  aggregationType: "SUM" | "AVG" | "COUNT" | "MIN" | "MAX" | "NONE" | "sum" | "avg" | "count" | "min" | "max" | "none";
  format?: string;
}

export interface TotalRow {
  id: string;
  label: string;
  type: TotalRowType;
  columns: TotalRowColumn[];
  position: "top" | "bottom";
  showLabel: boolean;
  headerTransform?: "DEFAULT" | "UPPER" | "LOWER";
  groupBy?: ("employee" | "month" | "week" | "none")[];
  labelTemplate?: string;
  showGroupContext?: boolean;
}

export interface ReportFormData {
  name: string;
  description: string;
  headerTransform?: "DEFAULT" | "UPPER" | "LOWER";
  reportType?: "ASSISTANCE" | "EMPLOYEE_DATA";
  fields: ReportField[];
  totalRows?: TotalRow[];
}

